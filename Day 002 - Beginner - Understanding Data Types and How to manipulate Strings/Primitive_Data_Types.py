# Data Types 

# String Data Type
print("Hello"[0]) # index of string which is start from 0.
print("Hello"[4]) # 4th character in this string is o .
print("123"[1]) 
print("123"+"345") # Simple String Concatination


# Integer Data Type
print(123+345) # adding two integer numbers.
print(123_345_567) # just like:  123,345,567


# Float Data Type
print(3.14159)  # decimal point - Floating point numbers
print(75.5,"%")


# Boolean Data Type

a = True
b = False
print(a,b)
